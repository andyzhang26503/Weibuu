- (void)requestMyStatus
{
    SinaWeibo *mysinaweibo = [SinaWeiboManager sinaweibo];
    if (mysinaweibo.isAuthValid) {
        [mysinaweibo requestWithURL:@"statuses/user_timeline.json"
                             params:nil
                         httpMethod:@"Get"
                           delegate:self];
        
    }
    
}

